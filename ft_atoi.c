int	ft_atoi(const char *str)
{
	long long	num;

	num = 0;
	while ((*str >= 9 && *str <= 13) || *str == 32)
		str++;
	if (*str == '-')
		return (-1);
	if (*str == '+')
		str++;
	while (*str >= '0' && *str <= '9')
	{
		num = num * 10 + (*str - '0');
		str++;
		if (num >= 99999)
			return (-1);
	}
	return ((int)(num));
}
