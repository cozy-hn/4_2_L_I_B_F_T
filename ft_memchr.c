#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	char	*tmp;

	tmp = (char *)s;
	while (n--)
	{
		if (*tmp == c)
			return (tmp);
		tmp++;
	}
	return ((void *)0);
}
